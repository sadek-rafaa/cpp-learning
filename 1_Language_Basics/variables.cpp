#include <iostream>
using namespace std;

int main () {

        int myNum = 5;               // Integer (whole number without decimals)
        double myFloatNumber = 5.99;    // Floating point number (with decimals)
        char myCharacter = 'D';         // Character
        string myString = "Hello";     // String (text)
        bool myBoolean = true;       // Boolean (true or false)


        cout << "int myNum :: "   << myNum   << "\n";
        cout << "double myFloa :: "   << myFloatNumber << "\n";
        cout << "char myCharacter :: "  <<  myCharacter << "\n";
        cout << "string myString :: "  <<  myString << "\n";
        cout << "bool myBoolea :: "   << myBoolean << "\n";

}